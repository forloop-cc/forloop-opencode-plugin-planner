import { tool } from '@opencode-ai/plugin';
import { ForLoopAPIClient } from '../capabilities/api-client';
import { resolveSprintId } from '../capabilities/context-resolver';
import { validateToken } from '../capabilities/auth';

export function createAgentSuggestTool(client: ForLoopAPIClient) {
  return tool({
    description: 'Get AI suggestions for story breakdowns, estimates, and planning',
    args: {
      type: tool.schema.enum([
        'breakdown',
        'estimate',
        'acceptance_criteria',
        'test_cases',
        'related',
        'sprint_planning',
      ]).describe('Type of suggestion needed'),
      sprintId: tool.schema.number()
        .optional()
        .describe('Sprint ID for context'),
      storyId: tool.schema.number()
        .optional()
        .describe('Story ID to analyze'),
      query: tool.schema.string()
        .optional()
        .describe('Specific question or requirement'),
    },
    async execute(args, _context) {
      const tokenResult = await validateToken();
      if (!tokenResult.valid) {
        return tokenResult.error;
      }
      client.setToken?.(tokenResult.token);

      if (!args.sprintId && !args.storyId) {
        return [
          '❌ Please provide sprintId or storyId',
          '',
          'Examples:',
          '  forloop.agent.suggest --type breakdown --sprintId 123',
          '  forloop.agent.suggest --type estimate --storyId 456',
        ].join('\n');
      }

      try {
        const response = await client.getAgentSuggestions({
          sprintId: args.sprintId,
          storyId: args.storyId,
          type: args.type,
          query: args.query,
        });

        const lines = [
          `🤖 AI Suggestions (${response.agentKey})`,
          `Confidence: ${(response.confidence * 100).toFixed(0)}%`,
          '',
          '**Suggestions:**',
          '',
        ];

        for (const suggestion of response.suggestions) {
          lines.push(`- ${suggestion}`);
        }

        return lines.join('\n');
      } catch (error: any) {
        return `❌ Error: ${error.message}`;
      }
    },
  });
}

