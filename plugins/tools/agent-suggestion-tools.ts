export function createConversationHistoryTool(client: ForLoopAPIClient) {
  return tool({
    description: 'View conversation history with AI agents from opencode sessions',
    args: {
      sprintId: tool.schema.number()
        .optional()
        .describe('Filter by sprint'),
      limit: tool.schema.number()
        .min(1)
        .max(200)
        .default(50)
        .describe('Number of messages to retrieve'),
    },
    async execute(args, context) {
      const tokenResult = await validateToken();
      if (!tokenResult.valid) {
        return tokenResult.error;
      }
      client.setToken?.(tokenResult.token);

      const resolution = await resolveSprintId(args.sprintId, context.directory);
      const sprintId = resolution.sprintId || args.sprintId;

      try {
        const history = await client.getConversationHistory({
          sprintId,
          limit: args.limit,
        });

        const messages = history.messages || [];
        if (messages.length === 0) {
          return 'No conversation history found.';
        }

        const lines = [
          `💬 Conversation History (${history.total || messages.length} total)`,
          '',
        ];

        for (const msg of messages.slice(0, args.limit)) {
          const preview = (msg.userMessage || 'No message').substring(0, 80);
          const hasResponse = msg.agentResponse && msg.agentResponse.trim();

          lines.push(`📌 **${preview}**${preview.length >= 80 ? '...' : ''}`);

          if (msg.conversationId) {
            const agentMatch = msg.conversationId.match(/agent:([^:]+)/);
            if (agentMatch) {
              lines.push(`   Agent: ${agentMatch[1]} | ${formatDate(msg.timestamp)}`);
            } else {
              lines.push(`   ${formatDate(msg.timestamp)}`);
            }
          } else {
            lines.push(`   ${formatDate(msg.timestamp)}`);
          }

          if (hasResponse) {
            const respPreview = msg.agentResponse.substring(0, 100);
            lines.push(`   → ${respPreview}${msg.agentResponse.length > 100 ? '...' : ''}`);
          }

          lines.push('');
        }

        return lines.join('\n');
      } catch (error: any) {
        return `❌ Error: ${error.message}`;
      }
    },
  });
}

export function createClearHistoryTool(client: ForLoopAPIClient) {
  return tool({
    description: 'Clear opencode conversation history for a sprint',
    args: {
      sprintId: tool.schema.number()
        .optional()
        .describe('Clear only for specific sprint'),
      confirm: tool.schema.boolean()
        .default(false)
        .describe('Confirm deletion'),
    },
    async execute(args, context) {
      const tokenResult = await validateToken();
      if (!tokenResult.valid) {
        return tokenResult.error;
      }
      client.setToken?.(tokenResult.token);

      if (!args.confirm) {
        return [
          '⚠️ Warning: This will permanently delete conversation history.',
          '',
          'Use --confirm true to proceed.',
          'Or specify --sprintId to delete only for a specific sprint.',
        ].join('\n');
      }

      const resolution = await resolveSprintId(args.sprintId, context.directory);
      const sprintId = resolution.sprintId || args.sprintId;

      try {
        const result = await client.clearConversationHistory({
          sprintId,
        });

        return `✅ Deleted ${result.count} messages successfully.`;
      } catch (error: any) {
        return `❌ Error: ${error.message}`;
      }
    },
  });
}

function formatDate(dateString: string): string {
  return new Date(dateString).toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
}
